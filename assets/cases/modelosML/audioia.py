import librosa
import numpy as np
import matplotlib.pyplot as plt

# Cargar audio
audio, sr = librosa.load('IA.wav')

# Aplicar ventana para evitar errores por bordes
windowed_audio = audio * np.hanning(len(audio))

# FFT
fft_result = np.fft.fft(windowed_audio)
frequencies = np.fft.fftfreq(len(fft_result), 1/sr)
magnitude = np.abs(fft_result)

# Filtrar solo la mitad positiva del espectro (frecuencias reales)
half_len = len(frequencies) // 2
frequencies = frequencies[:half_len]
magnitude = magnitude[:half_len]

# Graficar con límite en el eje x para mejor interpretación
plt.figure(figsize=(12, 6))
plt.plot(frequencies, magnitude)
plt.xlabel('Frecuencia (Hz)')
plt.ylabel('Magnitud')
plt.title('🎙️ Espectro de Frecuencias (0 - 5000 Hz)')
plt.xlim(0, 5000)  # Limitar a 0–5000 Hz
plt.grid(True)

# Líneas guía para referencia de graves y agudos
plt.axvline(x=300, color='blue', linestyle='--', label='Graves ≤ 300 Hz')
plt.axvline(x=2000, color='red', linestyle='--', label='Agudos ≥ 2000 Hz')
plt.legend()
plt.show()