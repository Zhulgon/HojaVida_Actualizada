import librosa
import librosa.display
import matplotlib.pyplot as plt
import numpy as np
from scipy.spatial.distance import euclidean

# Cargar audios
audio_guitarra, sr_guitarra = librosa.load('guitarra.wav')
audio_flauta, sr_flauta = librosa.load('flauta.wav')

# Extraer MFCC
mfcc_guitarra = librosa.feature.mfcc(y=audio_guitarra, sr=sr_guitarra, n_mfcc=13)
mfcc_flauta = librosa.feature.mfcc(y=audio_flauta, sr=sr_flauta, n_mfcc=13)

# Calcular la media de cada coeficiente MFCC
mfcc_guitarra_mean = np.mean(mfcc_guitarra, axis=1)
mfcc_flauta_mean = np.mean(mfcc_flauta, axis=1)

# Visualizar MFCC en el tiempo
plt.figure(figsize=(12, 6))

plt.subplot(2, 2, 1)
librosa.display.specshow(mfcc_guitarra, x_axis='time', sr=sr_guitarra)
plt.colorbar()
plt.title('MFCC - Guitarra')

plt.subplot(2, 2, 2)
librosa.display.specshow(mfcc_flauta, x_axis='time', sr=sr_flauta)
plt.colorbar()
plt.title('MFCC - Flauta')

# Graficar los MFCC promedios
plt.subplot(2, 1, 2)
plt.plot(mfcc_guitarra_mean, label='Guitarra (promedio)', marker='o')
plt.plot(mfcc_flauta_mean, label='Flauta (promedio)', marker='s')
plt.title("Comparación de MFCC Promedio")
plt.xlabel("Coeficiente MFCC")
plt.ylabel("Valor")
plt.grid(True)
plt.legend()

plt.tight_layout()
plt.show()

# Distancia entre las características
distancia = euclidean(mfcc_guitarra_mean, mfcc_flauta_mean)
print(f"Distancia Euclidiana entre MFCC promedio (Guitarra vs Flauta): {distancia:.2f}")

