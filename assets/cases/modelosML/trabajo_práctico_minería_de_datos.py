import numpy as np

def asignar_clusters(vigilancia):
    clusters = []

    for punto in subset:
        asignado = False

        # Revisar cada cluster ya creado
        for c in clusters:
            centro = np.mean(c, axis=0)

            # Similitud por coseno
            num = np.dot(punto, centro)
            den = np.linalg.norm(punto) * np.linalg.norm(centro)
            similitud = num / den

            if similitud >= vigilancia:
                c.append(punto)
                asignado = True
                break

        # Si no encontró cluster → crear uno nuevo
        if not asignado:
            clusters.append([punto])

    return clusters

clusters_09 = asignar_clusters(0.9)
