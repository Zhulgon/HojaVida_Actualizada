#Impor librerias necesarias
import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
from sklearn.datasets import make_blobs
from sklearn.cluster import KMeans

"""Ejercicio 2:

2.1 Crear dataset sintético (300 puntos, 3 clústeres)
"""

# Crear dataset con 300 puntos y 3 clusters
X, y = make_blobs(n_samples=300, centers=3, random_state=42)

# Convertir a DataFrame
df = pd.DataFrame(X, columns=['x', 'y'])

# Gráfico en dispersión
plt.scatter(df['x'], df['y'])
plt.title("Datos Sintéticos - Dispersión")
plt.xlabel("x")
plt.ylabel("y")
plt.show()

"""2.2. Aplicar K-Means con k = 3"""

# Crear el modelo
kmeans = KMeans(n_clusters=3, random_state=42)

# Ajustar el modelo
kmeans.fit(df)

# Obtener etiquetas
labels = kmeans.labels_

# Obtener centroides
centroids = kmeans.cluster_centers_

# Graficar clusters coloreados
plt.scatter(df['x'], df['y'], c=labels, cmap='viridis')
plt.scatter(centroids[:, 0], centroids[:, 1], s=200, c='red', marker='X')
plt.title("K-Means con k=3")
plt.xlabel("x")
plt.ylabel("y")
plt.show()

"""2.3. Cambiar K a 4"""

kmeans4 = KMeans(n_clusters=4, random_state=42)
kmeans4.fit(df)

labels4 = kmeans4.labels_
centroids4 = kmeans4.cluster_centers_

plt.scatter(df['x'], df['y'], c=labels4, cmap='rainbow')
plt.scatter(centroids4[:, 0], centroids4[:, 1], s=200, c='black', marker='X')
plt.title("K-Means con k=4")
plt.xlabel("x")
plt.ylabel("y")
plt.show()

